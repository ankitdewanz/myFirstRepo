package com.company;

public class CWH_18_SwitchCase {
    public static void main(String[] args) {
        //char ch= 'f';
        //char ch= 'd';
        char ch= 's';
        switch(ch){
            case 'd':
                System.out.println("Char is equal to 'd'");
                break;
            case 'f':
                System.out.println("Char is equal to 'f'");
                break;
            default:
                System.out.println("Char is different from 'd' and 'f'");
        }

    }
}
