package com.company;

public class CWH_16_IfElse {
    public static void main(String[] args) {
        int i = 1000;
        if (i> 100){
            System.out.println("The number "+i+ " is greater than 100");
        }
        else{
            System.out.println("The number "+i+ " is not greater than 100");
        }
    }
}
